def sf(run):
    run.font.name = '宋体'
    run._element.rPr.rFonts.set(qn('w:eastAsia'), '宋体')

def title(doc, text, size=14, bold=True):
    p = doc.add_paragraph()
    p.alignment = WD_ALIGN_PARAGRAPH.CENTER
    r = p.add_run(text)
    r.font.size = Pt(size); r.font.bold = bold
    r.font.name = '黑体'; r._element.rPr.rFonts.set(qn('w:eastAsia'), '黑体')
    return p

def heading(doc, text, size=12):
    p = doc.add_paragraph()
    r = p.add_run(text)
    r.font.size = Pt(size); r.font.bold = True
    r.font.name = '黑体'; r._element.rPr.rFonts.set(qn('w:eastAsia'), '黑体')
    return p

def para(doc, text, indent=0):
    p = doc.add_paragraph()
    r = p.add_run(text)
    r.font.size = Pt(10.5); sf(r)
    if indent: p.paragraph_format.left_indent = Cm(indent)
    return p

def choice(doc, num, text, opts):
    p = doc.add_paragraph()
    r = p.add_run(f'{num}．{text}'); r.font.size = Pt(10.5); sf(r)
    for o in opts:
        p = doc.add_paragraph()
        r = p.add_run(o); r.font.size = Pt(10.5); sf(r)
        p.paragraph_format.left_indent = Cm(0.74)

def img(doc, path, w_cm):
    p = doc.add_paragraph()
    r = p.add_run(); r.add_picture(path, width=Cm(w_cm))
    return p

def blank(doc, text, indent=0.5):
    p = doc.add_paragraph()
    r = p.add_run(text); r.font.size = Pt(10.5); sf(r)
    p.paragraph_format.left_indent = Cm(indent)
    return p

